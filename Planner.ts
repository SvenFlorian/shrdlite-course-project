    /**
     * Top-level driver for the Planner. Calls `planInterpretation` for each given interpretation generated by the Interpreter.
     * @param interpretations List of possible interpretations.
     * @param currentState The current state of the world.
     * @returns Augments Interpreter.InterpretationResult with a plan represented by a list of strings.
     */
    export function plan(interpretations : Interpreter.InterpretationResult[], currentState : WorldState) : PlannerResult[] {
        var errors : Error[] = [];
        var plans : PlannerResult[] = [];
        interpretations.forEach((interpretation) => {
            try {
                var result : PlannerResult = <PlannerResult>interpretation;
                result.plan = planInterpretation(result.interpretation, currentState);
                if (result.plan.length == 0) {
                    result.plan.push("That is already true!");
                }
                plans.push(result);
            } catch(err) {
                errors.push(err);
            }
        });
        if (plans.length) {
            return plans;
        } else {
            // only throw the first error found
            throw errors[0];
        }
    }

    export interface PlannerResult extends Interpreter.InterpretationResult {
        plan : string[];
    }

    export function stringify(result : PlannerResult) : string {
        return result.plan.join(", ");
    }

    function StringifyState(state : WorldState): string {
      var s :string = "";
      for(var i : number = 0; i < state.stacks.length; i++) {
        for(var j : number = 0; j < state.stacks.length; j++) {
          if(state.stacks[i][j] == undefined){
            continue;
          }
          s+=state.stacks[i][j];
        }
        s+="+";
      }
      s+=state.arm;
      s+=state.holding;
      return s;
    }
    function cloneStacks(s : Stack[]) : Stack[] { // A lot of computation will be done here
      var newStackList : Stack[] = new Array<Array<string>>();
      for(var i : number = 0; i < s.length ; i++) {
        var newStack : Stack = new Array<string>();
        for(var j : number = 0; j < s[i].length ; j++) {
          newStack.push(s[i][j]);
        }
        newStackList.push(newStack);
      }
      return newStackList;
    }
    function moveArm(state : WorldState, n :number) {
      state.arm += n;
    }
    function drop(state : WorldState) {
      state.stacks[state.arm].push(state.holding);
      state.holding = undefined;
    }
    function pickup(state : WorldState) {
      state.holding = state.stacks[state.arm].pop();
    }
    class StateGraph implements Graph<WorldState> {
      /** Computes the edges that leave from a node. */
      outgoingEdges(node : WorldState) : Edge<WorldState>[] {
        var edgeList : Edge<WorldState>[] = new Array<Edge<WorldState>>();
        var actions : String[] = getPossibleActions(node);
        for(var i :number = 0; i < actions.length; i++) {
          var newState : WorldState = {arm: node.arm, stacks: cloneStacks(node.stacks),
                holding: node.holding, objects : node.objects, examples : node.examples}
          switch (actions[i]) {
            case "r":
              moveArm(newState,1); //not sure if this is the right direction
              break;
            case "l":
              moveArm(newState,-1);
              break;
            case "d":
              drop(newState);
              break;
            case "p":
              pickup(newState);
              break;
          }
          var newEdge : Edge<WorldState> = new Edge<WorldState>();
          newEdge.from = node;
          newEdge.to = newState;
          newEdge.cost = 1;
          edgeList.push(newEdge);
        }
        return edgeList;
      }
      /** A function that compares nodes. */
      compareNodes(s1 : WorldState, s2 : WorldState) : number {
        if (s1.arm != s2.arm || s1.holding != s2.holding) {
          return 1;
        }
        for(var i : number = 0; i < s1.stacks.length; i++) {
          for(var j : number = 0; j < s1.stacks.length; j++) {
            if(s1.stacks[i][j] != s1.stacks[i][j]){
              return 1;
            }
          }
        }
        return 0;
      }
    }
    //returns all possible actions in the current world state, "r" "l" "p" "d"
    function getPossibleActions(w1 : WorldState) : string[] {
      var result : string[];
      var temp : string = w1.stacks[w1.arm].pop();
      w1.stacks[w1.arm].push(temp);
      var obj2 : ObjectDefinition = w1.objects[temp];
      var obj : ObjectDefinition = w1.objects[w1.holding];

      if(w1.arm != 0) {
        result.push("l");
      }
      if(w1.arm < w1.stacks.length) {
        result.push("r");
      }
      if(temp != null) {
        result.push("p");
      }
      if(!(obj.form == "ball" && obj2.form != null) ||
      (!((obj.form == "box" && obj.size == "small") && (obj2.size == "small" && (obj2.form == "brick" || obj2.form == "pyramid"))))||
      (!((obj.size == "large" && obj.form == "box") && (obj2.form == "pyramid"))) ||
      (!(obj2.size == "small" && obj.size == "large")) ||
      (!(obj2.form == "ball")) || (w1.stacks[w1.arm].length < 5)) {
        result.push("d");
      }
      return result;
    }

